async function main() {
    const [deployer] = await ethers.getSigners();

    console.log("Deploying contracts with the account:", deployer.address);

    console.log("Account balance:", (await deployer.getBalance()).toString());

    const JPEGusd = await ethers.getContractFactory("JPEGusd");
    const jpegusd = await JPEGusd.deploy();

    console.log("Contract address:", jpegusd.address);
  }

  main()
    .then(() => process.exit(0))
    .catch((error) => {
      console.error(error);
      process.exit(1);
    });
